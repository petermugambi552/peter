#include <iostream>
using namespace std;
int main()
{
    // create integer variables length and width
    int length = 40;
    int width = 60;
    // calculate area of a rectangle
    int area = length * width;
    // print the variables
    cout << "length is:" << length << "\n";
    cout << "width is:" << width << "\n";
    cout << "area of the rectangle is:" << area << "\n";
    return 0;
}